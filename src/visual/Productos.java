    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String a = jTextNombreNacional.getText();
        String b = jTextFechaVNacional.getText();
        String c = jTextFechaPNacional.getText();
        String d = jTextFormaVentaNacional.getText();
        String e = jTextCodigoNacional.getText();
        
        if (!a.equals("") && !b.equals("") && !c.equals("") && !d.equals("")
                && !e.equals("") && (!(jTextCantidadNacional.getText().equals("")))
                && (!(jTextPrecioNacional.getText().equals("")))) {
            int Cantidad = Integer.parseInt(jTextCantidadNacional.getText());
            double Precio = Double.parseDouble(jTextPrecioNacional.getText());
            //String formaVenta, String nombre, String FechaVencimiento, String FechaProduccion, double precio, int Cantidad
            fb.AddProducto(new ConsumoNacional(d, a, b, c, e, Precio, Cantidad));
            System.out.println("Producto Nacional Añadido Correctamente");
            try {
                fb.GuardarListProductos();
            } catch (IOException ex) {
                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            jTextNombreNacional.setText("");
            jTextFechaVNacional.setText("");
            jTextFechaPNacional.setText("");
            jTextCodigoNacional.setText("");
            jTextPrecioNacional.setText("");
            jTextCantidadNacional.setText("");
            jTextFormaVentaNacional.setText("");
            JOptionPane.showMessageDialog(rootPane, "Producto añadido correctamente");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Faltan campos por llenar");
        } // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

