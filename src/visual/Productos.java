    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // TODO add your handling code here:
            fb.CargarMaterias();
        } catch (IOException ex) {
            Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (!jComboBoxCodProd.getSelectedItem().toString().equals("")) {
            if (!nombre.getText().equals("") && !codigo.getText().equals("") && !precio.getText().equals("")) {
                if (mNacionales.isSelected()) {
                    if (!jTextField4.getText().equals("")) {
                        MP_Nacional nueva = new MP_Nacional(jTextField4.getText(), nombre.getText(), codigo.getText(), Double.valueOf(precio.getText()));
                        fb.AddMateriaPrimaDadoProducto(jComboBoxCodProd.getSelectedItem().toString(), nueva);
                        JOptionPane.showMessageDialog(rootPane, "Se a añadido correctamente la Materia Prima");
                        
            nombre.setText("");
            codigo.setText("");
            precio.setText("");
            jTextField4.setText("");
            
                        try {
                            fb.GuardarListMateriales(jComboBoxCodProd.getSelectedItem().toString(), nueva);
                        } catch (IOException ex) {
                            Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        nombre.setText("");
                        codigo.setText("");
                        precio.setText("");
                        jTextField4.setText("");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Faltan parametros por llenar");
                    }
                    
                } else {
                    if (mImportadas.isSelected()) {
                        if (!jTextField4.getText().equals("")) {
                            MP_Importada nueva = new MP_Importada(jTextField4.getText(), nombre.getText(), codigo.getText(), Double.valueOf(precio.getText()));
                            fb.AddMateriaPrimaDadoProducto(jComboBoxCodProd.getSelectedItem().toString(), nueva);
                            try {
                                fb.GuardarListMateriales(jComboBoxCodProd.getSelectedItem().toString(), nueva);
                            } catch (IOException ex) {
                                Logger.getLogger(Productos.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            
                            JOptionPane.showMessageDialog(rootPane, "Se a añadido correctamente la Materia Prima");
                            nombre.setText("");
                            codigo.setText("");
                            precio.setText("");
                            jTextField4.setText("");
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Faltan parametros por llenar");
                        }
                        
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Selecciona el tipo de Materia Prima");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Faltan parametros por llenar");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccione el codigo del producto al que pertenece la Materia Prima");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

